    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
         try {
    String searchText = jtxtsearch.getText();
    String sqlTotalAmount = "SELECT SUM(Amount) AS TotalAmount FROM ManagerBonuses WHERE " + 
            "BonusID LIKE ? OR ManagerID LIKE ? OR Bonus_Title LIKE ? OR Date LIKE ? OR Amount LIKE ?";

    PreparedStatement totalAmountStatement = con.prepareStatement(sqlTotalAmount);

    for (int i = 1; i <= 5; i++) {
        totalAmountStatement.setString(i, "%" + searchText + "%");
    }

    ResultSet resultSet = totalAmountStatement.executeQuery();

    if (resultSet.next()) {
        double totalAmount = resultSet.getDouble("TotalAmount");

        String formattedTotalAmount = String.format("%.2f", totalAmount);
        jtxtamountmanager.setText(formattedTotalAmount);
    } else {
        jtxtamountmanager.setText("0.00"); 
    }

    resultSet.close();
    totalAmountStatement.close();
} catch (SQLException ex) {
    JOptionPane.showMessageDialog(null, ex);
}
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String sql = "select * from ManagerBonuses";
        try{
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel) tblbonus.getModel();
            model.setRowCount(0);
            while(rs.next()){
                model.addRow(new String[]{rs.getString(1), rs.getString(2), rs.getString(3),rs.getString(4), rs.getString(5)});
            }
        }catch(Exception ex){
            System.out.println("Error : " +ex.getMessage());
        }       
    }//GEN-LAST:event_jButton4ActionPerformed

