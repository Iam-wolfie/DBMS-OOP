    private void CheckTotalEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckTotalEmployeeActionPerformed
        // TODO add your handling code here:
        try {
            String searchText = jtxtsearchbar.getText();
            String sqlTotalAmount = "SELECT SUM(Amount) AS TotalAmount FROM EmployeeBonuses WHERE " +
            "BonusID LIKE ? OR EmployeeID LIKE ? OR Bonus_Title LIKE ? OR Date LIKE ? OR Amount LIKE ?";

            PreparedStatement totalAmountStatement = con.prepareStatement(sqlTotalAmount);

            for (int i = 1; i <= 5; i++) {
                totalAmountStatement.setString(i, "%" + searchText + "%");
            }

            ResultSet resultSet = totalAmountStatement.executeQuery();

            if (resultSet.next()) {
                double totalAmount = resultSet.getDouble("TotalAmount");

                String formattedTotalAmount = String.format("%.2f", totalAmount);
                jtxtemployeeamount.setText(formattedTotalAmount);
            } else {
                jtxtemployeeamount.setText("0.00");
            }

            resultSet.close();
            totalAmountStatement.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_CheckTotalEmployeeActionPerformed

